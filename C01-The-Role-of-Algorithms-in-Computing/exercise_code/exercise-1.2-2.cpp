#include <iostream>
#include <cmath>

int main()
{
	// to avoid endless loop
	int maxNumber = 100;
	for (int i = 1; i < maxNumber; ++i)
	{
		if ((8 * i * i) < (64 * i * log(i) / log(2)))
		{
			std::cout << i << " ";
		}
	}
	std::cout << std::endl;
	return 0;
}
